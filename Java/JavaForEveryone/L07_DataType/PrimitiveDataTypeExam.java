package L07_DataType;

public class PrimitiveDataTypeExam {
    public static void main(String[] args) {
        boolean isFun = true;
        System.out.println("isFun : " + isFun);

        char c = 'f';
        int x = 59;
        long bing = 345455433L;
        float f = 32.4F;
        double d = 3434343.5;

        System.out.println("c : " + c + "\n" +
                "x : " + x + "\n" +
                "bing : " + bing + "\n" +
                "f : " + f + "\n" +
                "d : " + d + "\n");

    }
}
