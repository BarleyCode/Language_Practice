money=0; c500=0; c100=0; c50=0; c10=0;

money = int(input("금액 입력: "))

c500 = money // 500;
c100 = (money - (c500 * 500)) // 100;
c50 = (money - (c500 * 500) - (c100 * 100)) // 50;
c10 = (money -  (c500 * 500) - (c100 * 100) - (c50 * 50)) // 10;

print("500원: %d개" % c500)
print("100원: %d개" % c100)
print("50원: %d개" % c50)
print("10원: %d개" % c10)
