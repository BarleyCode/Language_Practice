while(1) :
    day = input("요일 입력 : ('끝' 입력 시 종료) : ")
    if (day == "월") :
        print("Monday")
    elif (day == "화") :
        print("Tuesday")
    elif (day == "수") :
        print("Wednesday")
    elif (day == "목") :
        print("Thursday")
    elif (day == "금") :
        print("Friday")
    elif (day == "토") :
        print("Saturday")
    elif (day == "일") :
        print("Sunday")
    elif (day == "끝") :
        break
    else :
        print("다시 입력하세요.")


