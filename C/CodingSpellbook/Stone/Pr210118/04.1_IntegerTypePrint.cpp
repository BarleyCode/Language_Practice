#include <cstdio>
int main() {
	printf("34+56\n");
	printf("%d\n", 34 + 56);
	printf("%d+%d=%d\n", 34, 56, 34 + 56);

	printf("34-56\n");
	printf("%d\n", 34 - 56);
	printf("%d-%d=%d\n", 34, 56, 34 - 56);

	printf("34*56\n");
	printf("%d\n", 34 * 56);
	printf("%d*%d=%d\n", 34, 56, 34 * 56);

	printf("34/56\n");
	printf("%d\n", 34 / 56);
	printf("%d/%d=%d\n", 34, 56, 34 / 56);
	return 0;
}